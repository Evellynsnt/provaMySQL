    public void atualizaTabela(){
        tabela.setRowCount(0); 
        ArrayList<Paciente> lista = pacienteController.listar();
        if(lista.size()>0){
                for (Paciente p : lista) {
                tabela.addRow(new Object[]{p.getIdPaciente(), p.getNomePaciente(), p.getCpf(), p.getTelefone()});
                }
            }
    }
